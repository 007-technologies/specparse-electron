#!/usr/bin/env node
/**
 * prepare-build.js
 *
 * Runs before electron-builder. Reads ANTHROPIC_API_KEY from env (or from the
 * local .api-key file as a fallback) and writes it into src/embeddedConfig.js.
 *
 * The generated embeddedConfig.js is gitignored. It's included in the electron-
 * builder bundle so the shipped .app / .exe has the key baked in — Spencer never
 * needs to enter one.
 *
 * In dev (`npm start`) the file may not exist or may have an empty key; main.js
 * falls back to the user-entered key in electron-store in that case.
 */
const fs = require('fs');
const path = require('path');

const projectRoot = path.resolve(__dirname, '..');

function readKey() {
  if (process.env.ANTHROPIC_API_KEY && process.env.ANTHROPIC_API_KEY.trim()) {
    return process.env.ANTHROPIC_API_KEY.trim();
  }
  // Fallback: .api-key file in project root
  const keyFile = path.join(projectRoot, '.api-key');
  if (fs.existsSync(keyFile)) {
    try {
      const v = fs.readFileSync(keyFile, 'utf8').trim();
      if (v) return v;
    } catch (_) { /* ignore */ }
  }
  return '';
}

const apiKey = readKey();
const outPath = path.join(projectRoot, 'src', 'embeddedConfig.js');

const buildDate = new Date().toISOString();
const pkg = JSON.parse(fs.readFileSync(path.join(projectRoot, 'package.json'), 'utf8'));

const content = `// AUTO-GENERATED by build/prepare-build.js — do not commit actual keys.
// Regenerated on every build from process.env.ANTHROPIC_API_KEY (or .api-key file).
module.exports = {
  embeddedApiKey: ${JSON.stringify(apiKey)},
  buildDate: ${JSON.stringify(buildDate)},
  version: ${JSON.stringify(pkg.version)},
};
`;

fs.writeFileSync(outPath, content, { mode: 0o600 });

if (apiKey) {
  const masked = apiKey.slice(0, 8) + '…' + apiKey.slice(-4);
  console.log('[prepare-build] Embedded API key (' + masked + ') into src/embeddedConfig.js');
} else {
  console.warn('[prepare-build] WARNING: no ANTHROPIC_API_KEY found in env or .api-key file.');
  console.warn('[prepare-build] The built app will require users to enter a key in Settings.');
}
console.log('[prepare-build] Build date: ' + buildDate);
console.log('[prepare-build] Version: ' + pkg.version);

// Ensure Windows .ico exists. electron-builder requires it for Windows target.
// We auto-generate from assets/icon_base.png on first build — afterwards the
// generated .ico is checked into the repo so CI/other machines don't need this.
const icoPath = path.join(projectRoot, 'assets', 'icon.ico');
const pngPath = path.join(projectRoot, 'assets', 'icon_base.png');
if (!fs.existsSync(icoPath) && fs.existsSync(pngPath)) {
  console.log('[prepare-build] assets/icon.ico missing — generating from icon_base.png');
  const { spawnSync } = require('child_process');
  const res = spawnSync('npx', ['--yes', 'png-to-ico', pngPath], {
    stdio: ['ignore', 'pipe', 'inherit'],
    cwd: projectRoot,
  });
  if (res.status === 0 && res.stdout && res.stdout.length > 0) {
    fs.writeFileSync(icoPath, res.stdout);
    console.log('[prepare-build] Generated assets/icon.ico (' + res.stdout.length + ' bytes)');
  } else {
    console.warn('[prepare-build] WARNING: could not generate assets/icon.ico');
    console.warn('[prepare-build] The Windows build will fail. Run manually: npx --yes png-to-ico assets/icon_base.png > assets/icon.ico');
  }
}
